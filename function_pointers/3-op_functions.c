#include "3-calc.h"

/**
 * op_add - function
 * @a: The first operand
 * @b: The second operand
 *
 * Adds two numbers
 *
 * Return: The result of the operation
 *
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function
 * @a: The first operand
 * @b: The second operand
 *
 * Substract two numbers
 *
 * Return: The result of the operation
 *
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function
 * @a: The first operand
 * @b: The second operand
 *
 * Multiplies two numbers
 *
 * Return: The result of the operation
 *
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function
 * @a: The first operand
 * @b: The second operand
 *
 * Divides two numbers
 *
 * Return: The result of the operation
 *
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function
 * @a: The first operand
 * @b: The second operand
 *
 * Modules two numbers
 *
 * Return: The result of the operation
 *
 */
int op_mod(int a, int b)
{
	return (a % b);
}

